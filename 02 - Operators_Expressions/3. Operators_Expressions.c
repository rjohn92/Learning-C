#include <stdio.h>


int main() {
    int a = 15; // Declare and initialize an integer variable a with the value 15
    int b = 6;  // Declare and initialize an integer variable b with the value 5
    int sum = a +b ; // Calculate the sum of a and b and store it in the variable sum
    printf("Sum: %d\n", sum); // Print the result using printf with format specifiers for integers
    printf("Product: %d\n", a * b); 
    printf("Quotient: %d\n", a / b);
    printf("Remainder: %d\n", a % b);
    return 0; // Return 0 to indicate successful execution of the program
}
