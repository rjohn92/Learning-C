#include <stdio.h> // This line includes the standard input-output library, which is necessary for using printf.

int main() {
    int age = 32; // This line declares an integer variable named age and initializes it with the value 32.
    printf("Hello, CNO World! I am %d years old.\n", age); // This line prints a message to the console. The %d format specifies the integer variable at the end.
    age = 33; // This line updates the value of the age variable to 33.
    printf("Next year, I will be %d years old.\n", age); //

    char initial = 'C'; // This line declares a character variable named initial and initializes it with the character 'C'.
    printf("CNO start with the letter %c.\n", initial); // This line prints a message to the console, using %c to format the character variable. 
    
    return 0; // This line ends the main function and returns 0 to the operating system. This indicates that the program executed successfully.
}


