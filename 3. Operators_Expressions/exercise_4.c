#include <stdio.h>

int main() {
    unsigned int a = 5; // Declare an unsigned integer variable a with the value 5
    unsigned int b = 3; // Declare an unsigned integer variable b with the value 3
    unsigned int and_result = a & b; // Calculate the sum of a and b
    printf("a & b =  %u\n", and_result); // Print the result using printf with format specifiers for unsigned integers

}